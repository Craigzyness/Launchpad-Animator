// Generates the 8x8 grid of MIDI note numbers for the Launchpad MK2
const generateGridNotes = (): number[][] => {
  const grid: number[][] = [];
  for (let y = 8; y >= 1; y--) {
    const row: number[] = [];
    for (let x = 1; x <= 8; x++) {
      row.push(y * 10 + x);
    }
    grid.push(row);
  }
  return grid;
};

export const GRID_NOTES: number[][] = generateGridNotes();

